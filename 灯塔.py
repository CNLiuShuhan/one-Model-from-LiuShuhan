from turtle import*
setup(1000,650)
color("black","yellow")
up()
goto(0,160)
down()
begin_fill()
goto(-400,240)
pencolor("yellow")
goto(-400,80)
pencolor("black")
goto(0,160)
end_fill()
up()
goto(-40,160)
setheading(270)
down()
fillcolor("tomato")
begin_fill()
circle(40)
end_fill()
fillcolor("floral white")
begin_fill()
goto(40,160)
goto(80,-200)
goto(-80,-200)
goto(-40,160)
end_fill()
up()
goto(0,-200)
down()
fillcolor("green yellow")
begin_fill()
setheading(0)
a=[40,80,40,80]
for i in a:
    forward(i)
    left(90)
end_fill()
y=[120,80,30]
for c in range(0,3):
    up()
    goto(-30-5*c,y[c])
    down()
    begin_fill()
    for i in range(0,4):
        forward(20+10*c)
        right(90)
    end_fill()
up()
goto(4,-162)
down()
color("black","yellow")
begin_fill()
circle(4)
end_fill()
hideturtle()
done()
